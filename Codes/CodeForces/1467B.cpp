int *dp;
ll *a;
ll n;

int isValley(int i) {
  return i > 0 && i < n - 1 && a[i-1]>a[i] && a[i] < a[i+1];
}

int isHill(int i) {
  return i > 0 && i < n - 1 && a[i-1]<a[i] && a[i] > a[i+1];
}

void start() {
  cin>>n;
  a = new ll[n+1];
  a[n] = 0;
  for(int i=0; i<n; i++) {  cin>>a[i];  }
  
  dp = new int[n+1];
  dp[0] = 0; dp[n] = 0;
  
  ll s = 0;
  
  for(int i=0; i<n; i++) {
    if(isHill(i) || isValley(i) ) {
      dp[i] = 1; s++; 
    }else{
      dp[i] = 0;
    }
  }

  ll minimus = s;

  for(int i=1; i<n-1; i++) {
    ll temp = a[i];
    a[i] = a[i-1];
    minimus = min(minimus, s - dp[i-1] - dp[i] - dp[i+1] 
      + isHill(i-1) + isHill(i) + isHill(i+1) 
      + isValley(i-1) + isValley(i) + isValley(i+1));

    a[i] = a[i+1];
    minimus = min(minimus, s - dp[i-1] - dp[i] - dp[i+1] 
      + isHill(i-1) + isHill(i) + isHill(i+1) 
      + isValley(i-1) + isValley(i) + isValley(i+1));

    a[i] = temp;
  }
  cout<<minimus<<"\n";
  delete[] dp;
  delete[] a;
}

