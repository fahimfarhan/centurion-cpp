unordered_map<int, vector<int> > g;
unordered_map<int, bool> isvisited;
unordered_set<int> allNodes;
unordered_map<int, int> level;
int t;

void bfs(int src) {

  for(auto node : allNodes) {
    isvisited[node] = false;
  }

  level[src] = 0;
  queue<int> q;
  q.push(src);
  while(!q.empty()) {
    int u = q.front();
    q.pop();

    isvisited[u] = true;
    int nextLevel = (level[u] + 1);

    for(int i=0; i<g[u].size(); i++) {
      int v = g[u][i];
      if(!isvisited[v]) {
        q.push(v);
        level[v] = nextLevel;
      }
    }
  }
}

void start() {
  int N, u, v;
  int src, unReachableKount, ttl;

  cin>>N;
  if(N == 0) {
    exit(0);
  }
  for(int i=0; i<N; i++) {
    cin>>u>>v;
    g[u].push_back(v);
    g[v].push_back(u);
    allNodes.insert(u); 
    allNodes.insert(v);
  }

  int allNodesKount = allNodes.size();
  while(true) {
    cin>>src>>ttl;
    if(src == 0 && ttl == 0) {  break; }
    // else perform a bfs

    for(auto node: allNodes) {
      level[node] = INT_MAX;
    }

    bfs(src);
    unReachableKount = 0;
    for(auto node: allNodes) {
      if(level[node] > ttl) {
        unReachableKount++;
      }
    }

    int unvisitedKount = 0;
    for(auto node: allNodes) {
      if(!isvisited[node]) {
        unvisitedKount++;
      }
    }

    unReachableKount = unReachableKount + unvisitedKount; 

    // Case 1: 5 nodes not reachable from node 35 with TTL = 2.
    t++;
    cout<<"Case "<<t<<": "<<unReachableKount<<" nodes not reachable from node "<<src<<" with TTL = "<<ttl<<".\n";
  }

  allNodes.clear();
  g.clear();
  isvisited.clear();
}

