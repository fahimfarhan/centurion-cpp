class MyQueue{
public:
  stack<int> s1, s2;
  MyQueue() {}

  void enque(int input) {
    s1.push(input);
  }

  void dequeue() {
    if(s2.empty()) {
      // 1
      while (!s1.empty()) {
        int top = s1.top();
        s1.pop();
        s2.push(top);
      }
    }

    // 2 dequeue
    s2.pop();
  }

  int front() {
    int front = 0;
    if(s2.empty()) {
      // 1
      while (!s1.empty()) {
        int top = s1.top();
        s1.pop();
        s2.push(top);
      }
    }

    // dequeue
    front = s2.top();
    return front;
  }
};

