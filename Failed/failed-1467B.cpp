int T, n;
ll *a;
short *dp;  

inline int isHill(const int& i) {
  if( (i == 0) || (i == (n-1)) ) return 0;
  if( (a[i-1] < a[i]) && (a[i] > a[i+1] ) ) {
    return 1;
  }
  return 0;
}

inline int isValley(const int& i) {
  if( (i == 0) || (i == (n-1)) ) return 0;
  if( (a[i-1] > a[i]) && (a[i] < a[i+1] ) ) {
    return 1;
  }
  return 0;
}

inline void preprocess(int& extremaCount) {
  dp[0] = 0; dp[n-1] = 0;
  for(int i=1; i<n-1; i++) {
    if(isHill(a[i]) || isValley(a[i]) ) {
      dp[i] = 1;
      extremaCount++;
    }else{
      dp[i] = 0;
    }
  }
}

inline int getCount(const int& extremaCount , const int& i) {
  int ret = extremaCount - dp[i-1] - dp[i] - dp[i+1] 
            + isHill((i-1)) + isHill(i) + isHill( (i+1) ) 
            + isValley((i-1)) + isValley(i) + isValley((i+1));
  return ret;
}

inline void start() {
  cin>>n;
  a = new ll[n+1];
  dp = new short[n+1];
  for(int i=0; i<n; i++) {  cin>>a[i];  }

  int extremaCount = 0;
  preprocess(extremaCount);
  
  ll temp;
  int ans = extremaCount;
  for(int i=1; i<n-1; i++) {
    temp = a[i];

    a[i] = a[i-1];
    ans = min(ans,   getCount(extremaCount, i) );

    a[i] = a[i+1];
    ans = min(ans,   getCount(extremaCount, i) );

    a[i] = temp;
  }

  cout<<ans<<"\n";

    

  delete[] a;
  delete[] dp;
}

