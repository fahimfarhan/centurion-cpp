#define N 10001
bool isNonPrime[N+1]; // by default false. reinit will only kill time
vector<int> primes;

inline void seivePreprocess() {
  isNonPrime[0] = true;
  isNonPrime[1] = true;

  for(int i=4; i<=N; i+=2) {
    isNonPrime[i] = true;
  }

  for(int i=3; i*i<=N; i+=2) {
    if(isNonPrime[i] == false) {
      for(int j=i*i; j<=N; j+=i) {
        isNonPrime[j] = true;
      } 
    }
  }

  for(int i=0; i<=N; i++) {
    if(isNonPrime[i] == false) {
      primes.push_back(i);
    }
  }
}

int main(int argc, char const *argv[]){
  /* code */
  FastIO();

  seivePreprocess();

  int T, L, R;
  // cin>>T;
  T = 1;
  vector<int>::iterator lb;
  vector<int>::iterator ub;
  while(T--) {
    cin>>L>>R;

    lb = lower_bound(primes.begin(), primes.end(), L);
    ub = upper_bound(primes.begin(), primes.end(), R);

    // cout<<*lb<<"\n";
    // cout<<*ub<<"\n";
    for(auto it = lb; it != ub; it++) {
      cout<<(*it)<<" ";
    }cout<<"\n";
  }

  return 0;
}