#define N 1000001
bool isNonPrime[N+1]; // by default srt to false. do not reinit as it kills time
// vector<int> primes;
int primePrimes[N+1];

inline void preprocess() {
  isNonPrime[0] = true;
  isNonPrime[1] = true;

  for(int i=4; i<=N; i+=2) {  isNonPrime[i] = true; }

  for(int i=3; i*i <=N; i+=2) {
    if(isNonPrime[i] == false) {
      for(int j=i*i; j<=N; j+=i) {
        isNonPrime[j] = true;
      }
    }
  }

  int kount = 0;
  for(int i=0; i<N; i++) {
    if(isNonPrime[i] == false) {
      // primes.push_back(i);
      kount++;
    }
    if(isNonPrime[kount] == false)
      primePrimes[i] = 1;
    else primePrimes[i] = 0;
  }

  for(int i=1; i<N; i++) {
    primePrimes[i] += primePrimes[i-1];
  }



}

